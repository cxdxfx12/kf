export class KnowledgeBaseService {
  private items: KnowledgeItem[] = knowledgeBase;

  search(query: string): KnowledgeItem[] {
    if (!query || query.trim() === '') {
      return this.items;
    }

    const keywords = query.toLowerCase().split(/\s+/).filter(k => k.length > 0);
    const normalizedQuery = query.toLowerCase();

    return this.items
      .map(item => {
        let score = 0;

        if (item.title.toLowerCase().includes(normalizedQuery)) {
          score += 10;
        }
        if (item.content.toLowerCase().includes(normalizedQuery)) {
          score += 5;
        }
        if (item.category.toLowerCase().includes(normalizedQuery)) {
          score += 3;
        }

        keywords.forEach(keyword => {
          if (item.keywords.some(k => k.toLowerCase().includes(keyword))) {
            score += 4;
          }
          if (item.title.toLowerCase().includes(keyword)) {
            score += 2;
          }
          if (item.content.toLowerCase().includes(keyword)) {
            score += 1;
          }
        });

        return { ...item, score };
      })
      .filter(item => (item as any).score > 0)
      .sort((a, b) => (b as any).score - (a as any).score)
      .slice(0, 20);
  }

  getCategories(): string[] {
    return [...new Set(this.items.map(item => item.category))];
  }

  getByCategory(category: string): KnowledgeItem[] {
    return this.items.filter(item => item.category === category);
  }

  getById(id: string): KnowledgeItem | undefined {
    return this.items.find(item => item.id === id);
  }
}

export const knowledgeBaseService = new KnowledgeBaseService();
